    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String descripcion = null, tipo, estado;
        double precio;
        int stock;
        if(txtDescripcion.getText().compareTo("")!=0 && cboTipo.getSelectedIndex()!=0 && 
                txtPrecio.getText().compareTo("")!=0 && txtStock.getText().compareTo("")!=0 && 
                cboEstado.getSelectedIndex()!=0){
            descripcion = txtDescripcion.getText();
            tipo = cboTipo.getSelectedItem().toString();
            precio = Double.parseDouble(txtPrecio.getText());
            stock = Integer.parseInt(txtStock.getText());
            estado = cboEstado.getSelectedItem().toString();
            if(reg){
                try {
                    Platillo platillo = new Platillo(descripcion, tipo, stock, precio,estado);
                    GestionarPlatilloServicio gestionarPlatilloServicio = new GestionarPlatilloServicio();
                    gestionarPlatilloServicio.crearPlatillo(platillo);
                    JOptionPane.showMessageDialog(this,"Se guardo el Platillo","Informacion",JOptionPane.INFORMATION_MESSAGE);
                    limpiarCampos();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,e.getMessage()+" Error","No se pudo ingresar el Platillo",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                try {
                    Platillo platillo = new Platillo(descripcion, tipo, stock, precio,estado);
                    GestionarPlatilloServicio gestionarPlatilloServicio = new GestionarPlatilloServicio();
                    gestionarPlatilloServicio.modificarPlatillo(platillo);
                    JOptionPane.showMessageDialog(this,"Se modific√≥ el Platillo","Informacion",JOptionPane.INFORMATION_MESSAGE);
                    limpiarCampos();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,e.getMessage()+" Error","No se pudo modificar el Platillo",JOptionPane.ERROR_MESSAGE);
                }   
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

